package Praktikum14;
public class Customer7 {
    public int id;
    public String name;

    public Customer7() {
    }

    public Customer7(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return " ID: " + this.id + " Name: " + this.name;
    }
}